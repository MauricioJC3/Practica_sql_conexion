<script>
document.addEventListener('DOMContentLoaded', () => {
    const tabLinks = document.querySelectorAll('.tab-link');
    const tabPanes = document.querySelectorAll('.tab-pane');

    tabLinks.forEach(link => {
        link.addEventListener('click', (e) => {
            e.preventDefault();

            // Remove active class from all tab links
            tabLinks.forEach(link => link.classList.remove('active', 'text-white', 'bg-blue-600'));
            tabLinks.forEach(link => link.classList.add('text-gray-900'));

            // Hide all tab panes
            tabPanes.forEach(pane => pane.classList.add('hidden'));
            tabPanes.forEach(pane => pane.classList.remove('show', 'active'));

            // Show the clicked tab pane
            const target = document.querySelector(link.getAttribute('data-tab-target'));
            target.classList.remove('hidden');
            target.classList.add('show', 'active');

            // Add active class to the clicked tab link
            link.classList.add('active', 'text-white', 'bg-blue-600');
            link.classList.remove('text-gray-900');
        });
    });
});


</script>
